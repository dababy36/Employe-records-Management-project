package com.employe_management.erms.enums;

public enum employementStatus {
    ACTIVE, // this is the default status of an employee
    INACTIVE, // employee is not currently working but still employed
    ON_LEAVE, //employee is not working due to leave (vacation, sick, etc)
    SUSPENDED, //employee is not working due to suspension
    FIRED //employee is no longer employed
    
}
